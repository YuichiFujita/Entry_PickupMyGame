#include "main.h"

//**********************************************************************************************************************
//	列挙型定義 (SOUNDTYPE)
//**********************************************************************************************************************
typedef enum
{
	SOUNDTYPE_NONE = 0,				// 効果音無し
	SOUNDTYPE_EXPLOSION,			// 爆発音
	SOUNDTYPE_BREAK,				// 破壊音
	SOUNDTYPE_MAX,					// この列挙型の総数
} SOUNDTYPE;

//**********************************************************************************************************************
//	プロトタイプ宣言
//**********************************************************************************************************************
void InitExplosion(void);			// 爆発の初期化処理
void UninitExplosion(void);			// 爆発の終了処理
void UpdateExplosion(void);			// 爆発の更新処理
void DrawExplosion(void);			// 爆発の描画処理
void SetExplosion(D3DXVECTOR3 pos, SOUNDTYPE sound);	// 爆発の設定処理

//**********************************************************************************************************************
//	プロトタイプ宣言 (デバッグ用)
//**********************************************************************************************************************
int GetNumExplosion(void);			// 爆発の総数取得処理

#endif