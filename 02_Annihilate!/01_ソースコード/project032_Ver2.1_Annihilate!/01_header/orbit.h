#ifndef _ORBIT_H_			// このマクロ定義がされていない場合
#define _ORBIT_H_			// 二重インクルード防止のマクロを定義する

//**********************************************************************************************************************
//	マクロ定義
//**********************************************************************************************************************
#define NONE_ORBIT	(-1)	// 軌跡の初期値 (使用していない場合)

//**********************************************************************************************************************
//	列挙型定義 (OFFSETTYPE)
//**********************************************************************************************************************
typedef enum
{
	OFFSETTYPE_ENEMY_HEIGHT = 0,	// 敵のオフセット (縦)
	OFFSETTYPE_ENEMY_WIDTH,			// 敵のオフセット (横)
	OFFSETTYPE_MAX,					// この列挙型の総数
} OFFSETTYPE;

//**********************************************************************************************************************
//	列挙型定義 (ORBITTYPE)
//**********************************************************************************************************************
typedef enum
{
	ORBITTYPE_NONE = 0,	// 軌跡 (テクスチャなし)
	ORBITTYPE_NORMAL,	// 軌跡 (通常)
	ORBITTYPE_MAX,		// この列挙型の総数
} ORBITTYPE;

//**********************************************************************************************************************
//	プロトタイプ宣言
//**********************************************************************************************************************
void InitOrbit(void);	// 軌跡の初期化処理
void UninitOrbit(void);	// 軌跡の終了処理
void UpdateOrbit(void);	// 軌跡の更新処理
void DrawOrbit(void);	// 軌跡の描画処理
int  SetOrbit(D3DXMATRIX *pMtxParent, bool *pUse, int *pOrbitID, D3DXCOLOR col, OFFSETTYPE offType, ORBITTYPE texType);	// 軌跡の設定処理

//**********************************************************************************************************************
//	プロトタイプ宣言 (デバッグ用)
//**********************************************************************************************************************

#endif