#ifndef _SLASH_H_			// このマクロ定義がされていない場合
#define _SLASH_H_			// 二重インクルード防止のマクロを定義する

//**********************************************************************************************************************
//	インクルードファイル
//**********************************************************************************************************************
#include "main.h"

//**********************************************************************************************************************
//	マクロ定義
//**********************************************************************************************************************
#define NONE_SLASH		(-1)	// 斬撃の初期値 (使用していない場合)
#define SLA_DMG_WEED	(50)	// 雑草に一律にかかるダメージ量

//**********************************************************************************************************************
//	列挙型定義 (SLASHTYPE)
//**********************************************************************************************************************
typedef enum
{
	SLASHTYPE_PLAYER = 0,	// プレイヤーの斬撃
	SLASHTYPE_ENEMY,		// 敵の斬撃
	SLASHTYPE_MAX			// この列挙型の総数
} SLASHTYPE;

//**********************************************************************************************************************
//	プロトタイプ宣言
//**********************************************************************************************************************
void InitSlash(void);		// 斬撃の初期化処理
void UninitSlash(void);		// 斬撃の終了処理
void UpdateSlash(void);		// 斬撃の更新処理
void DrawSlash(void);		// 斬撃の描画処理
int  SetSlash(SLASHTYPE type, int nLife, float fRadius, int *pSlashID, bool *pUse);	// 斬撃の設定処理
void SetPositionSlash(int nSlashID, D3DXVECTOR3 pos, D3DXVECTOR3 rot);				// 斬撃の位置・向きの設定処理

//**********************************************************************************************************************
//	プロトタイプ宣言 (デバッグ用)
//**********************************************************************************************************************
int GetNumSlash(void);		// 斬撃の総数取得処理

#endif